// Generates netlify/edge-functions/chat.ts from analysis.md so the edge
// function can ship with the AI prompt baked in. Runs as `prebuild` so the
// edge-function copy of the prompt stays in sync with the source.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";

const analysis = readFileSync("./analysis.md", "utf8");
const promptLiteral = JSON.stringify(analysis);

const out = `// AUTO-GENERATED by scripts/build-edge-functions.mjs from analysis.md.
// Edit analysis.md and re-run \`bun run build\` — direct edits here will be
// overwritten on the next build.
//
// Why an Edge Function: Netlify's regular Functions buffer responses through
// the AWS Lambda gateway, which silently kills any fetch that goes >10s
// without bytes (the "Inactivity Timeout" page). Edge Functions run on Deno
// Deploy, stream natively, and get a 50s execution window — exactly what we
// need to wait for the first byte from Gemini and then forward chunks.

declare const Deno: { env: { get: (k: string) => string | undefined } };

type ChatMsg = { role: "user" | "assistant" | "system"; content: string };
type Payload = { messages: ChatMsg[]; tradesContext: string };
type GeminiStreamFrame = {
  candidates?: Array<{ content?: { parts?: Array<{ text?: string }> } }>;
};

const ANALYSIS_PROMPT = ${promptLiteral};

const MODEL = Deno.env.get("GEMINI_MODEL") || "gemini-2.5-flash";

const plain = (text: string, status = 200) =>
  new Response(text, {
    status,
    headers: { "Content-Type": "text/plain; charset=utf-8", "Cache-Control": "no-store" },
  });

export default async (request: Request): Promise<Response> => {
  if (request.method !== "POST") return plain("Method not allowed", 405);

  let data: Payload;
  try {
    data = (await request.json()) as Payload;
  } catch {
    return plain("Invalid JSON body", 400);
  }
  if (!data || !Array.isArray(data.messages)) {
    return plain("Invalid payload: missing messages[]", 400);
  }

  const apiKey = Deno.env.get("GEMINI_API_KEY");
  if (!apiKey) {
    return plain("AI is not configured. Please set GEMINI_API_KEY in your environment.");
  }

  const now = new Date();
  const iso = (d: Date) => d.toISOString().slice(0, 10);
  const daysAgo = (n: number) => {
    const d = new Date(now);
    d.setDate(d.getDate() - n);
    return iso(d);
  };
  const referenceDates = [
    \`Today: \${iso(now)}\`,
    \`Yesterday: \${daysAgo(1)}\`,
    \`7 days ago: \${daysAgo(7)}\`,
    \`30 days ago: \${daysAgo(30)}\`,
    \`90 days ago: \${daysAgo(90)}\`,
    \`Start of this month: \${iso(new Date(now.getFullYear(), now.getMonth(), 1))}\`,
    \`Start of this year: \${iso(new Date(now.getFullYear(), 0, 1))}\`,
  ].join("\\n");

  const systemText = \`\${ANALYSIS_PROMPT}

---

Reference dates (authoritative — use these, not your training cutoff):
\${referenceDates}

The user's trade journal data (JSON):
\${data.tradesContext}\`;

  // Gemini uses "user" / "model" roles (not "assistant"); system prompt goes
  // in a top-level systemInstruction field.
  const contents = data.messages.map((m) => ({
    role: m.role === "assistant" ? "model" : "user",
    parts: [{ text: m.content }],
  }));

  const url = \`https://generativelanguage.googleapis.com/v1beta/models/\${MODEL}:streamGenerateContent?alt=sse&key=\${encodeURIComponent(apiKey)}\`;

  let upstream: Response;
  try {
    upstream = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        systemInstruction: { parts: [{ text: systemText }] },
        contents,
        generationConfig: { temperature: 0.3 },
      }),
    });
  } catch (e) {
    return plain(\`Gemini fetch failed: \${e instanceof Error ? e.message : "unknown"}\`);
  }

  if (!upstream.ok || !upstream.body) {
    const t = await upstream.text().catch(() => "");
    if (upstream.status === 429) return plain("Rate limit reached. Please try again in a moment.");
    if (upstream.status === 401 || upstream.status === 403 || /API_KEY_INVALID/i.test(t)) {
      return plain(
        "Gemini auth failed — your GEMINI_API_KEY is invalid. Get a fresh key at https://aistudio.google.com/apikey.",
      );
    }
    return plain(\`Gemini error (\${upstream.status}): \${t.slice(0, 300)}\`);
  }

  const encoder = new TextEncoder();
  const out = new ReadableStream<Uint8Array>({
    async start(controller) {
      // Send a leading byte immediately so any intermediary proxy sees
      // activity within the first millisecond — we don't want to depend on
      // Gemini's first-token latency to keep the connection alive.
      controller.enqueue(encoder.encode(""));

      const reader = upstream.body!.getReader();
      const decoder = new TextDecoder();
      let buffer = "";
      try {
        while (true) {
          const { value, done } = await reader.read();
          if (done) break;
          buffer += decoder.decode(value, { stream: true });
          const frames = buffer.split("\\n\\n");
          buffer = frames.pop() ?? "";
          for (const frame of frames) {
            const line = frame.trim();
            if (!line.startsWith("data:")) continue;
            const payload = line.slice(5).trim();
            if (!payload || payload === "[DONE]") continue;
            try {
              const parsed = JSON.parse(payload) as GeminiStreamFrame;
              const text =
                parsed.candidates?.[0]?.content?.parts
                  ?.map((p) => p.text)
                  .filter((t): t is string => Boolean(t))
                  .join("") ?? "";
              if (text) controller.enqueue(encoder.encode(text));
            } catch {
              // Ignore malformed frames — Gemini occasionally emits keepalives.
            }
          }
        }
      } catch (e) {
        controller.enqueue(
          encoder.encode(
            \`\\n\\n_(stream error: \${e instanceof Error ? e.message : "unknown"})_\`,
          ),
        );
      } finally {
        controller.close();
      }
    },
  });

  return new Response(out, {
    headers: {
      "Content-Type": "text/plain; charset=utf-8",
      "Cache-Control": "no-store",
      "X-Accel-Buffering": "no",
    },
  });
};

export const config = { path: "/api/chat" };
`;

mkdirSync("./netlify/edge-functions", { recursive: true });
writeFileSync("./netlify/edge-functions/chat.ts", out, "utf8");
console.log("✓ Generated netlify/edge-functions/chat.ts");
